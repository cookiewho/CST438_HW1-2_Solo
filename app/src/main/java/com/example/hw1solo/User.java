package com.example.hw1solo;

public class User {
        private String id;
        private String name;
        private String username;
        private String email;


        public String getId() {
                return id;
        }

        public String getName() {
                return name;
        }

        public String getUsername() {
                return username;
        }

        public String getEmail() {
                return email;
        }
}
